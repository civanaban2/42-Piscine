#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	tmp;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (nb < 0)
	{
		write(1, &"-", 1);
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	tmp = nb % 10 + '0';
	write(1, &tmp, 1);
}
